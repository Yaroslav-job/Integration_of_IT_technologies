func hireArmy(c *city.City) {
	fmt.Printf("–°–µ–π—á–∞—Å –≤ –≥–æ—Ä–æ–¥–µ: %d –≤–æ–∏–Ω–æ–≤\n", c.Army)
	fmt.Println("–°—Ç–æ–∏–º–æ—Å—Ç—å –æ–¥–Ω–æ–≥–æ –≤–æ–∏–Ω–∞: 10 –∑–æ–ª–æ—Ç–∞")
	fmt.Printf("–ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ –¥–æ—Å—Ç—É–ø–Ω–æ: %d (–∂–∏—Ç–µ–ª–µ–π: %d)\n", int(c.Residents/3), int(c.Residents))
	fmt.Print("–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–∏–Ω–æ–≤ –¥–ª—è –Ω–∞–π–º–∞ (–∏–ª–∏ 0 –¥–ª—è –æ—Ç–º–µ–Ω—ã): ")
	var count int
	fmt.Scan(&count) // —Å—á–∏—Ç—ã–≤–∞–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –≤–æ–∏–Ω–æ–≤ –¥–ª—è –Ω–∞–π–º–∞
	if count > 0 && c.Treasury >= count*10 {
		c.Treasury -= count * 10      // —É–º–µ–Ω—å—à–µ–Ω–∏–µ –∫–∞–∑–Ω—ã –Ω–∞ —Å—Ç–æ–∏–º–æ—Å—Ç—å –≤–æ–∏–Ω–æ–≤
		c.Army += count               // —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –∞—Ä–º–∏–∏ –Ω–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–∏–Ω–æ–≤
		c.Residents -= float64(count) // —É–º–µ–Ω—å—à–µ–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∂–∏—Ç–µ–ª–µ–π
		c.UpdateIncome()              // –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –¥–æ—Ö–æ–¥–∞
		c.CheckCityLevel()            // –ø—Ä–æ–≤–µ—Ä–∫–∞ —É—Ä–æ–≤–Ω—è –≥–æ—Ä–æ–¥–∞
	} else if count > 0 {
		fmt.Println("–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∑–æ–ª–æ—Ç–∞.")
	}
}
